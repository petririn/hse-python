"""

Задача 5. Сложная проверка с регулярными выражениями

Условие:
Напишите программу, которая анализирует сложность пароля используя тернарный оператор и regex.

Логика:

    Если пароль содержит цифры И 
    буквы в верхнем и нижнем регистре И длиной >= 8 -> "Надежный пароль"
    Иначе если пароль содержит цифры ИЛИ буквы в разных регистрах -> 
    "Средний пароль"  
    Иначе -> "Слабый пароль"

Пример входных данных:
    Пароль: "Secure123"
"""

password = "Secure123"

has_digit = bool(re.search(r'\d', password))          
has_lower = bool(re.search(r'[a-z]', password))      
has_upper = bool(re.search(r'[A-Z]', password))     
length_ok = len(password) >= 8

is_strong = has_digit and has_lower and has_upper and length_ok
has_mixed_case = has_lower and has_upper
is_medium = has_digit or has_mixed_case

result = (
    "Надежный пароль" if is_strong else
    "Средний пароль" if is_medium else
    "Слабый пароль"
)

print(result)

